const INTERVAL_MS = 5 * 60 * 1000;

/**
 * Returns a fixture ForecastResponse with all timestamps anchored to TODAY's
 * UTC midnight. Index 0 = 00:00 UTC today, preserving the daily MOER shape.
 */
export function makeFixtureForecast(): ForecastResponse {
  const todayUtcMidnight = new Date();
  todayUtcMidnight.setUTCHours(0, 0, 0, 0);
  const base = todayUtcMidnight.getTime();

  return {
    region: "CAISO_NORTH",
    generated_at: new Date(base).toISOString(),
    interval_minutes: 5,
    source: "fixture",
    points: MOER_DATA.map(([moer, health], i) => ({
      start: new Date(base + i * INTERVAL_MS).toISOString(),
      end: new Date(base + (i + 1) * INTERVAL_MS).toISOString(),
      moer_lbs_per_mwh: moer,
      health_score: health,
    })),
  };
}
